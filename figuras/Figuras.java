package figuras;

public abstract class Figuras {

	//Atributos
	public double x;
	public double y;
	
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}

//Constructor	
public abstract duoble area() {};

}